char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	ch;

	ch = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == ch)
			return ((char *)s + i);
		else
			i++;
	}
	if (ch == '\0')
		return ((char *)s + i);
	return (0);
}
